        <script>
            // const globalSearch = document.getElementById("keyword");

            const keywordFilter = document.getElementById("keywordFilter");
            const customer = document.getElementById("customer");
            const status = document.getElementById("status");
            const dateFrom = document.getElementById("dateFrom");
            const dateTo = document.getElementById("dateTo");
            const resetBtn = document.getElementById("resetBtn");

            const table = document.getElementById("invoiceTable");
            const tbody = table.querySelector("tbody");

            const allRows = Array.from(
                tbody.querySelectorAll("tr")
            );

            const pageCtrl = document.getElementById("paginationControls");
            const pageTxt = document.getElementById("paginationText");

            const perPage = 10;
            let currentPage = 1;

            function getFilteredRows() {

                const keyword = keywordFilter.value.toLowerCase();
                const cust = customer.value.toLowerCase();
                const stat = status.value.toLowerCase();
                const from = dateFrom.value;
                const to = dateTo.value;

                return allRows.filter(row => {

                    const invoiceNo =
                        row.cells[1].textContent.toLowerCase();

                    const customerName =
                        row.cells[2].textContent.toLowerCase();

                    const issueDate =
                        row.cells[3].textContent.trim();

                    const rowStatus =
                        row.querySelector(".badge")
                            .textContent
                            .trim()
                            .toLowerCase();

                    if (keyword &&
                        !invoiceNo.includes(keyword))
                        return false;

                    if (cust &&
                        customerName !== cust)
                        return false;

                    if (stat &&
                        rowStatus !== stat)
                        return false;

                    if (from &&
                        issueDate < from)
                        return false;

                    if (to &&
                        issueDate > to)
                        return false;

                    return true;

                });

            }

            function render() {

                const filteredRows = getFilteredRows();

                const total = filteredRows.length;

                const pages =
                    Math.max(
                        1,
                        Math.ceil(total / perPage)
                    );

                currentPage =
                    Math.min(currentPage, pages);

                const start =
                    (currentPage - 1) * perPage;

                allRows.forEach(row => {
                    row.style.display = "none";
                });

                filteredRows
                    .slice(start, start + perPage)
                    .forEach(row => {
                        row.style.display = "";
                    });

                pageTxt.textContent =
                    total === 0
                        ? "No data avaiable"
                        : `Show ${start + 1}–${Math.min(start + perPage, total)} from ${total} data`;

                pageCtrl.innerHTML = "";

                function addPage(label, targetPage, opts = {}) {

                    const li = document.createElement("li");

                    li.className =
                        "page-item" +
                        (opts.active ? " active" : "") +
                        (opts.disabled ? " disabled" : "");

                    if (opts.disabled) {
                        li.innerHTML = `<span class="page-link">${label}</span>`;
                    } else {
                        li.innerHTML = `<a class="page-link" href="#">${label}</a>`;
                        li.addEventListener("click", e => {
                            e.preventDefault();
                            currentPage = targetPage;
                            render();
                        });
                    }

                    pageCtrl.appendChild(li);

                }

                // Previous
                addPage("Previous", currentPage - 1, { disabled: currentPage <= 1 });

                // Page 1
                addPage("1", 1, { active: currentPage === 1 });

                // Titik awal
                if (currentPage > 3) {
                    addPage("...", null, { disabled: true });
                }

                // Page sekitar current
                for (let p = Math.max(2, currentPage - 1); p <= Math.min(pages - 1, currentPage + 1); p++) {
                    addPage(String(p), p, { active: currentPage === p });
                }

                // Titik akhir
                if (currentPage < pages - 2) {
                    addPage("...", null, { disabled: true });
                }

                // Last page
                if (pages > 1) {
                    addPage(String(pages), pages, { active: currentPage === pages });
                }

                // Next
                addPage("Next", currentPage + 1, { disabled: currentPage >= pages });

            }

            [
                keywordFilter,
                customer,
                status,
                dateFrom,
                dateTo
            ].forEach(el => {

                el.addEventListener(
                    "input",
                    () => {
                        currentPage = 1;
                        render();
                    }
                );

            });

            resetBtn.addEventListener(
                "click",
                () => {

                    keywordFilter.value = "";
                    customer.value = "";
                    status.value = "";
                    dateFrom.value = "";
                    dateTo.value = "";

                    currentPage = 1;

                    render();

                }
            );

            render();
        </script>
